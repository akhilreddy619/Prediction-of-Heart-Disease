    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try
        {
          ArrayList temp=new ketan.auth.DataOperations().GetPids();
          Object []headers={"Total","Positive","Negative"};
          trained=new ketan.auth.DataOperations().GetTrained();
          String record="";
          
          _pve=0;
          _nve=0;
         for(int i=0;i<temp.size();i++) 
         {
               record=new ketan.auth.DataOperations().GetSymptoms(temp.get(i).toString());
               for(int j=0;j<trained.size();j++)
               {
                    if(trained.get(j).equals(record))
                    {
                        _pve++;
                        break;
                    }
               }
         }
         _nve=(trained.size()-(_pve));
         Object [][]data=new Object[1][3];
         data[0][0]=trained.size()+"";
         data[0][1]=_pve+"";
         data[0][2]=_nve+"";
         
         TableModel tm=new DefaultTableModel(data, headers);
         jTable1.setModel(tm);
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this,"Error In Precision and Recall");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Object []headers={"Proposed Alg","Sensitivity","Specificity","Accuracy"};
        Object [][]data=new Object[1][4];
        
        data[0][0]="MLPNN With BPNN";
        _se=(double)_pve/(_pve+_nve)*100;
        _sp=(double)(_nve/_nve)*100;
        data[0][1]=_se+"";
        data[0][2]=_sp+"";
        _acc=(double)(_pve + _nve)/trained.size()*100;
        data[0][3]=_acc+"";
        
         TableModel tm=new DefaultTableModel(data, headers);
         jTable2.setModel(tm);
    }//GEN-LAST:event_jButton3ActionPerformed

