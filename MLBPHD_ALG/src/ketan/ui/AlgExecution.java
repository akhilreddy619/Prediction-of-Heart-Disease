    private void btnApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyActionPerformed
        // TODO add your handling code here:
        try {
            //get inputorgans for the patient
            pstmt=con.createStatement();
            
            rs=pstmt.executeQuery("select AGE,SEX,CHEST_PAIN,BP,CHOLESTROL,FBloodsugar,ECG,HEARTRATE,EXERcISE_habit,SLOPE,VESSELS,SCAN,HEREDITY from tblsymptoms where pat_id like '" + cmbPatients.getSelectedItem().toString() + "'");
            if(rs.next()) {
                for(int i=1;i<=13;i++)
                    input[i-1][0]=rs.getDouble(i);
            } else
                throw new Exception("Record Not matched");
            
            for(int i=0;i<input.length;i++)
                txtIP.append(input[i][0] +"\r\n");
            
            op[0][0]=Math.random()*2-1;
            lblOP.setText(op[0][0]+"");
            ApplyBpn();
            lblOP.setText(BackPrg._top);
                    
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(this,"Error In Alg Application");
        }
}//GEN-LAST:event_btnApplyActionPerformed

