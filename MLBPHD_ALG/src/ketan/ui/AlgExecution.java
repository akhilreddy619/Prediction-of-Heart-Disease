   private void loadPatients()
    {
        try
        {
            pstmt=con.createStatement();
            rs=pstmt.executeQuery("select distinct pat_id from tblsymptoms where pat_id not in(select pat_id from tblreports)");
            cmbPatients.removeAllItems();
            cmbPatients.addItem("--Select--");
            while(rs.next())
            {
                cmbPatients.addItem(rs.getString(1));
            }
            rs.close();
            pstmt.close();
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this,"Error " + ex.getMessage());
        }
    }
    
