    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            pstmt=con.prepareStatement("insert into tblPatients values(?,?,?,?,?,?,?)");
            
            pstmt.setString(1,txtPId.getText());
            pstmt.setString(2,txtPname.getText());
            pstmt.setString(3,txtPage.getText());
            pstmt.setString(4,txtMobno.getText());
            pstmt.setString(5,txtRefDoctor.getText());
            pstmt.setString(6,txtRegDate.getText());
            pstmt.setString(7,gender);
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this,"New Patient Details Saved");
            JOptionPane.showMessageDialog(this,"Now Feed Symptoms");
            jButton1.setEnabled(false);
        } catch(Exception ex) {
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this,"Error Occurred" );
        }
}//GEN-LAST:event_jButton1ActionPerformed

